/**
 * Given matrix where you have to find cats by ears "^^"
 *
 * @param {Array<Array>} matrix 
 * @return {Number} count of cats found
 *
 * @example
 * countCats([
 *  [0, 1, '^^'],
 *  [0, '^^', 2],
 *  ['^^', 1, 2]
 * ]) => 3`
 *
 */
function countCats(cats) {
  let it = 0;
  for (let i in cats) {
    it += cats[i].filter(item => item == "^^").length;
  }
  return it;
}

module.exports = {
  countCats
};
